#!/usr/bin/env python3
"""
Module containing the add function.
"""

def add(a: float, b: float) -> float:
    """
    Adds two float numbers and returns the result as a float.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
